"""Evaluate Chokepoint scorers on held-out test labels.

Ground truth: `coverage_drop` simulated by removing a vendor and counting the
fraction of (agency, naics) pairs it served that lose all suppliers. Labels
are generated by `pipeline/labels.py` over a candidate pool (top vendors by
footprint) with a deterministic train/test split.

This script:
  * Loads vendor_scores + vendor_labels.
  * Restricts evaluation to the TEST split — the supervised model was never
    trained on these rows.
  * Defines positives as test-split vendors with coverage_drop in the top
    quartile of the FULL pool (i.e. structurally meaningful chokepoints).
  * Reports Recall@k for baseline / iso / model.
  * Reports Spearman rank correlation of each scorer vs true coverage_drop
    on the test split. This is the cleanest "did we learn anything?" signal.
  * Writes eval_report.json and prints a summary table.
"""
from __future__ import annotations

import json
import logging
import pickle
import random
from collections import defaultdict
from datetime import datetime, timezone
from pathlib import Path

import networkx as nx
import numpy as np
import pandas as pd
from scipy.stats import spearmanr

from pipeline.critical_naics import CRITICAL_NAICS, critical_naics_id

GRAPH_PATH = Path("data/processed/supply_graph.pkl")
SCORES_PATH = Path("data/processed/vendor_scores.parquet")
LABELS_PATH = Path("data/processed/vendor_labels.parquet")
INJECTED_PATH = Path("data/synthetic/injected_chokepoints.json")
REPORT_PATH = Path("eval_report.json")

K_VALUES = (5, 10, 20, 50)
# Positives = top-quartile by coverage_drop AMONG nonzero pool vendors. Using
# the full pool would put the cutoff at 0 since most pool vendors have no
# chokepoint signal at all.
POSITIVE_QUANTILE = 0.75
BOOTSTRAP_ITERS = 1000
BOOTSTRAP_SEED = 1337

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
)
log = logging.getLogger("eval")


def build_pair_coverage(g: nx.MultiDiGraph) -> dict[tuple[str, str], set[str]]:
    vendor_agencies: dict[str, set[str]] = defaultdict(set)
    vendor_naics: dict[str, set[str]] = defaultdict(set)
    for u, v, d in g.edges(data=True):
        et = d.get("edge_type")
        if et == "VENDOR_AGENCY":
            vendor_agencies[u].add(v)
        elif et == "VENDOR_NAICS":
            vendor_naics[u].add(v)
    cov: dict[tuple[str, str], set[str]] = defaultdict(set)
    for vid, agencies in vendor_agencies.items():
        for a in agencies:
            for n in vendor_naics.get(vid, ()):
                cov[(a, n)].add(vid)
    return cov


def top_vulnerable_naics(
    base_cov: dict[tuple[str, str], set[str]], vendor_id: str, g: nx.MultiDiGraph
) -> list[str]:
    """List up to 5 NAICS descriptions where this vendor would zero-out coverage."""
    loss_count: dict[str, int] = defaultdict(int)
    for (_, n), holders in base_cov.items():
        if holders == {vendor_id}:
            loss_count[n] += 1
    top = sorted(loss_count.items(), key=lambda x: x[1], reverse=True)[:5]
    return [g.nodes[nid].get("description") or g.nodes[nid].get("code") or nid for nid, _ in top]


def recall_at_k(ranked: list[str], positives: set[str], k: int) -> float:
    if not positives:
        return 0.0
    return len(set(ranked[:k]) & positives) / len(positives)


def bootstrap_recall_ci(
    test_df: pd.DataFrame,
    score_col: str,
    positives_mask_col: str,
    k_values: tuple[int, ...] = K_VALUES,
    n_iters: int = BOOTSTRAP_ITERS,
    seed: int = BOOTSTRAP_SEED,
) -> dict[str, tuple[float, float, float]]:
    """Percentile bootstrap CIs for Recall@k.

    Resamples the test rows with replacement, re-ranks, and recomputes
    Recall@k each iteration. Returns (mean, lo, hi) at 95% level keyed by
    'recall@k'.
    """
    rng = np.random.default_rng(seed)
    n = len(test_df)
    if n == 0 or not test_df[positives_mask_col].any():
        return {f"recall@{k}": (0.0, 0.0, 0.0) for k in k_values}

    recalls: dict[int, list[float]] = {k: [] for k in k_values}
    for _ in range(n_iters):
        idx = rng.integers(0, n, size=n)
        sample = test_df.iloc[idx]
        positives = set(sample.loc[sample[positives_mask_col], "vendor_id"])
        if not positives:
            continue
        ranked = (
            sample.sort_values(score_col, ascending=False, kind="stable")["vendor_id"]
            .tolist()
        )
        for k in k_values:
            recalls[k].append(recall_at_k(ranked, positives, k))

    out: dict[str, tuple[float, float, float]] = {}
    for k in k_values:
        arr = np.array(recalls[k]) if recalls[k] else np.array([0.0])
        out[f"recall@{k}"] = (
            float(arr.mean()),
            float(np.percentile(arr, 2.5)),
            float(np.percentile(arr, 97.5)),
        )
    return out


def main() -> None:
    for p in (GRAPH_PATH, SCORES_PATH, LABELS_PATH):
        if not p.exists():
            raise SystemExit(
                f"Missing {p}. Run `make graph && make features && make labels && make train` first."
            )

    log.info("Loading graph + scores + labels")
    with open(GRAPH_PATH, "rb") as f:
        g: nx.MultiDiGraph = pickle.load(f)
    scores = pd.read_parquet(SCORES_PATH)
    labels = pd.read_parquet(LABELS_PATH)

    # Pool = train + test rows in labels
    pool = labels[labels["split"].isin(("train", "test"))]
    test = labels[labels["split"] == "test"]
    log.info(
        "Pool size=%d  train=%d  test=%d",
        len(pool),
        int((labels["split"] == "train").sum()),
        len(test),
    )

    nonzero_pool = pool[pool["coverage_drop"] > 0]
    if len(nonzero_pool) > 4:
        drop_threshold = float(nonzero_pool["coverage_drop"].quantile(POSITIVE_QUANTILE))
    else:
        drop_threshold = 0.0
    log.info(
        "Positive threshold: coverage_drop >= %.4f "
        "(top %d%% of %d nonzero pool vendors)",
        drop_threshold,
        int((1 - POSITIVE_QUANTILE) * 100),
        len(nonzero_pool),
    )

    test_positives = set(
        test[(test["coverage_drop"] >= drop_threshold) & (test["coverage_drop"] > 0)][
            "vendor_id"
        ]
    )
    log.info("Test positives: %d / %d", len(test_positives), len(test))

    # Restrict ranking to the test split so each scorer is judged on the same
    # held-out vendors.
    test_scored = scores[scores["vendor_id"].isin(test["vendor_id"])].merge(
        test[
            [
                "vendor_id",
                "coverage_drop",
                "critical_coverage_drop",
                "critical_naics_affected_count",
            ]
        ],
        on="vendor_id",
    )
    test_scored["is_positive"] = test_scored["vendor_id"].isin(test_positives)

    # Critical-mode positives: nonzero critical_coverage_drop in test split
    test_scored["is_critical_positive"] = test_scored["critical_coverage_drop"] > 0
    n_critical_positives = int(test_scored["is_critical_positive"].sum())
    log.info("Test critical-positives (critical_coverage_drop > 0): %d", n_critical_positives)

    rankers = {
        "baseline": test_scored.sort_values("baseline_score", ascending=False)["vendor_id"].tolist(),
        "iso": test_scored.sort_values("iso_score", ascending=False)["vendor_id"].tolist(),
        "model": test_scored.sort_values("model_score", ascending=False)["vendor_id"].tolist(),
    }

    recall_results: dict[str, dict[str, float]] = {}
    recall_ci: dict[str, dict[str, tuple[float, float, float]]] = {}
    spearman_results: dict[str, float] = {}
    critical_recall_results: dict[str, dict[str, float]] = {}
    for name in rankers:
        recall_results[name] = {
            f"recall@{k}": recall_at_k(rankers[name], test_positives, k) for k in K_VALUES
        }
        score_col = "baseline_score" if name == "baseline" else f"{name}_score"
        recall_ci[name] = bootstrap_recall_ci(
            test_scored, score_col, "is_positive"
        )
        rho, _ = spearmanr(test_scored[score_col], test_scored["coverage_drop"])
        spearman_results[name] = float(rho) if rho == rho else 0.0
        # Critical-mode recall@k against critical positives
        critical_positives_set = set(
            test_scored.loc[test_scored["is_critical_positive"], "vendor_id"]
        )
        critical_recall_results[name] = {
            f"recall@{k}": recall_at_k(rankers[name], critical_positives_set, k)
            for k in K_VALUES
        }

    # Pool-wide ground-truth top list (for the demo top chokepoints panel)
    base_cov = build_pair_coverage(g)
    name_by_id = dict(zip(scores["vendor_id"], scores["vendor_name"]))
    pool_sorted = pool.sort_values("coverage_drop", ascending=False).head(20)
    ground_truth_top = [
        {
            "vendor": name_by_id.get(row.vendor_id, row.vendor_id),
            "coverage_drop": float(row.coverage_drop),
            "naics_affected": int(row.naics_affected_count),
            "critical_coverage_drop": float(row.critical_coverage_drop),
            "critical_naics_affected": int(row.critical_naics_affected_count),
            "split": row.split,
        }
        for row in pool_sorted.itertuples()
    ]

    # Critical-mode pool-wide leaderboard
    critical_pool_sorted = (
        pool[pool["critical_coverage_drop"] > 0]
        .sort_values("critical_coverage_drop", ascending=False)
        .head(15)
    )
    critical_ground_truth_top = [
        {
            "vendor": name_by_id.get(row.vendor_id, row.vendor_id),
            "critical_coverage_drop": float(row.critical_coverage_drop),
            "critical_naics_affected": int(row.critical_naics_affected_count),
            "coverage_drop": float(row.coverage_drop),
            "split": row.split,
        }
        for row in critical_pool_sorted.itertuples()
    ]

    # Model's top-flagged chokepoints with live sim context (filter to candidate pool)
    pool_ids = set(pool["vendor_id"])
    pool_scored = scores[scores["vendor_id"].isin(pool_ids)].merge(
        pool[["vendor_id", "coverage_drop", "split"]], on="vendor_id"
    )
    model_top = pool_scored.sort_values("model_score", ascending=False).head(10)
    top_chokepoints = []
    for row in model_top.itertuples():
        top_chokepoints.append(
            {
                "vendor": row.vendor_name,
                "model_score": float(row.model_score),
                "iso_score": float(row.iso_score),
                "baseline_score": float(row.baseline_score),
                "coverage_drop": float(row.coverage_drop),
                "split": row.split,
                "top_vulnerable_naics": top_vulnerable_naics(base_cov, row.vendor_id, g),
            }
        )

    INJECTED_PATH.parent.mkdir(parents=True, exist_ok=True)
    INJECTED_PATH.write_text(
        json.dumps(
            {
                "rule_positives": (
                    f"test-split vendors with coverage_drop >= pool quantile "
                    f"{POSITIVE_QUANTILE} (~top {int((1-POSITIVE_QUANTILE)*100)}%)"
                ),
                "drop_threshold": drop_threshold,
                "pool_size": int(len(pool)),
                "test_size": int(len(test)),
                "test_positives": [
                    {"vendor": name_by_id.get(vid), "vendor_id": vid}
                    for vid in test_positives
                ],
            },
            indent=2,
        )
    )
    log.info("Wrote %s", INJECTED_PATH)

    report = {
        "ground_truth_rule": (
            f"Held-out test split (size={len(test)}). Positives = "
            f"coverage_drop >= pool quantile {POSITIVE_QUANTILE} "
            f"(threshold={drop_threshold:.4f})."
        ),
        "n_test_positives": len(test_positives),
        "n_test_critical_positives": n_critical_positives,
        "n_test": len(test),
        "recall_at_k": recall_results,
        "recall_at_k_95ci": {
            name: {k: {"mean": v[0], "lo": v[1], "hi": v[2]} for k, v in vals.items()}
            for name, vals in recall_ci.items()
        },
        "spearman_vs_true_coverage_drop": spearman_results,
        "critical_recall_at_k": critical_recall_results,
        "critical_naics_definition": {
            "n_codes": len(CRITICAL_NAICS),
            "codes": CRITICAL_NAICS,
            "provenance": (
                "DoD USD(R&E) Critical Technology Areas (2022/2023) + DPA Title III "
                "categories + NDAA 2022 Sec. 855"
            ),
        },
        "top_chokepoints": top_chokepoints,
        "ground_truth_top_20": ground_truth_top,
        "critical_ground_truth_top": critical_ground_truth_top,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "n_vendors_total": int(len(scores)),
        "candidate_pool_size": int(len(pool)),
    }
    REPORT_PATH.write_text(json.dumps(report, indent=2, default=str))
    log.info("Wrote %s", REPORT_PATH)

    # Pretty print
    print("\n=== Chokepoint Eval Summary ===")
    print(
        f"Vendors total={len(scores)}  pool={len(pool)}  "
        f"test={len(test)}  test_positives={len(test_positives)}"
    )
    print(f"\nRecall@k (held-out test split) — point estimates:")
    print(f"{'k':<6}{'baseline':>12}{'iso':>10}{'model':>10}")
    for k in K_VALUES:
        print(
            f"{k:<6}"
            f"{recall_results['baseline'][f'recall@{k}']:>12.3f}"
            f"{recall_results['iso'][f'recall@{k}']:>10.3f}"
            f"{recall_results['model'][f'recall@{k}']:>10.3f}"
        )
    print(f"\nRecall@k 95% bootstrap CI (n={BOOTSTRAP_ITERS}):")
    for name in ("baseline", "iso", "model"):
        cis = recall_ci[name]
        parts = [
            f"r@{k}={cis[f'recall@{k}'][0]:.2f}[{cis[f'recall@{k}'][1]:.2f},"
            f"{cis[f'recall@{k}'][2]:.2f}]"
            for k in K_VALUES
        ]
        print(f"  {name:<10} " + "  ".join(parts))

    print(f"\nSpearman(score, true coverage_drop) on test split:")
    for name, rho in spearman_results.items():
        print(f"  {name:<10} {rho:.3f}")

    print(
        f"\nCritical-mode Recall@k (positives = critical_coverage_drop>0, "
        f"n={n_critical_positives}):"
    )
    print(f"{'k':<6}{'baseline':>12}{'iso':>10}{'model':>10}")
    for k in K_VALUES:
        print(
            f"{k:<6}"
            f"{critical_recall_results['baseline'][f'recall@{k}']:>12.3f}"
            f"{critical_recall_results['iso'][f'recall@{k}']:>10.3f}"
            f"{critical_recall_results['model'][f'recall@{k}']:>10.3f}"
        )

    if critical_ground_truth_top:
        print(f"\nCritical-NAICS chokepoints (top 5 by critical_coverage_drop):")
        for row in critical_ground_truth_top[:5]:
            print(
                f"  {row['vendor'][:42]:<42} "
                f"crit_drop={row['critical_coverage_drop']:.3f}  "
                f"crit_naics={row['critical_naics_affected']}"
            )
    print(f"\nGround-truth top 5 chokepoints (pool-wide):")
    for row in pool_sorted.head(5).itertuples():
        print(
            f"  {name_by_id.get(row.vendor_id, '')[:42]:<42} "
            f"drop={row.coverage_drop:.3f}  naics_lost={row.naics_affected_count}"
        )
    print(f"\nModel top 5 flagged (in pool):")
    for row in top_chokepoints[:5]:
        print(
            f"  {row['vendor'][:42]:<42} score={row['model_score']:.3f}  "
            f"drop={row['coverage_drop']:.3f}  split={row['split']}"
        )


if __name__ == "__main__":
    main()
